    private void btn_hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hitungActionPerformed
        String nama;
        String nim;
        double nilaiTugas;
        double nilaiKuis;
        double nilaiUTS;
        double nilaiUAS;

        try {
            nama = txt_nama.getText();
            nim = txt_nim.getText();
            nilaiTugas = Double.parseDouble(txt_nilaitugas.getText());
            nilaiKuis = Double.parseDouble(txt_kuis.getText());
            nilaiUTS = Double.parseDouble(txt_nilaiuts.getText());
            nilaiUAS = Double.parseDouble(txt_nilaiuas.getText());
        } catch (NumberFormatException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Input nilai tidak valid");
            return;
        }
    double rerata = (nilaiTugas + nilaiKuis + nilaiUTS + nilaiUAS) / 4;
    
    char grade;
    if (rerata >= 85) {
        grade = 'A';
    } else if (rerata >= 70) {
        grade = 'B';
    } else if (rerata >= 55) {
        grade = 'C';
    } else if (rerata >= 40) {
        grade = 'D';
    } else {
        grade = 'E';
    }

    String keterangan = (rerata >= 55) ? "Lulus" : "Tidak Lulus";

    lbl_nama.setText(nama);
    lbl_nim.setText(nim);
    lbl_rerata.setText(String.valueOf(rerata));
    lbl_grade.setText(String.valueOf(grade));
    lbl_keterangan.setText(keterangan);
    }//GEN-LAST:event_btn_hitungActionPerformed


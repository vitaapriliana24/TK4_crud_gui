    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        String nama;
        String nim;
        double nilaiTugas;
        double nilaiKuis;
        double nilaiUTS;
        double nilaiUAS;

        try {
            nama = txt_nama.getText();
            nim = txt_nim.getText();
            nilaiTugas = Double.parseDouble(txt_nilaitugas.getText());
            nilaiKuis = Double.parseDouble(txt_kuis.getText());
            nilaiUTS = Double.parseDouble(txt_nilaiuts.getText());
            nilaiUAS = Double.parseDouble(txt_nilaiuas.getText());
        } catch (NumberFormatException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Input nilai tidak valid");
            return;
        }
    double rerata = (nilaiTugas + nilaiKuis + nilaiUTS + nilaiUAS) / 4;
    char grade;
    if (rerata >= 85) {
        grade = 'A';
    } else if (rerata >= 70) {
        grade = 'B';
    } else if (rerata >= 55) {
        grade = 'C';
    } else if (rerata >= 40) {
        grade = 'D';
    } else {
        grade = 'E';
    }
    String keterangan = (rerata >= 55) ? "Lulus" : "Tidak Lulus";

    String url = "jdbc:mysql://localhost:3306/nilai_mahasiswa_db";
    String user = "root";
    String password = "";

    String query = "INSERT INTO nilai_mahasiswa (nama, nim, nilai_tugas, nilai_kuis, nilai_uts, nilai_uas, rerata, grade, keterangan) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";

    try (Connection conn = DriverManager.getConnection(url, user, password);
         PreparedStatement pstmt = conn.prepareStatement(query)) {

        pstmt.setString(1, nama);
        pstmt.setString(2, nim);
        pstmt.setDouble(3, nilaiTugas);
        pstmt.setDouble(4, nilaiKuis);
        pstmt.setDouble(5, nilaiUTS);
        pstmt.setDouble(6, nilaiUAS);
        pstmt.setDouble(7, rerata);
        pstmt.setString(8, String.valueOf(grade));
        pstmt.setString(9, keterangan);

        pstmt.executeUpdate();
        javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
    } catch (SQLException e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data");
    }
    }//GEN-LAST:event_btn_simpanActionPerformed

